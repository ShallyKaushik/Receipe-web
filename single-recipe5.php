            <!-- single instruction -->
            <div class="single-instruction">
              <header>
                <p>Step 1</p>
                <div></div>
              </header>
              <p>
                <b>Prepare the Sugar Syrup:</b> In a pan, mix sugar and water. Heat the mixture
                 until the sugar dissolves completely. Add cardamom, saffron, and rose water. 
                 Let it simmer for 5 minutes, then turn off the heat and set aside.
              </p>
            </div>
            <!-- end of single instruction -->
            <!-- single instruction -->
            <div class="single-instruction">
              <header>
                <p>Step 2</p>
                <div></div>
              </header>
              <p>
                <b>Make the Dough:</b>
                In a bowl, mix milk powder, flour, baking soda, and ghee. Gradually 
                add milk and knead into a soft, smooth dough. Avoid over-kneading. Cover and let it rest 
                for 10 minutes.
              </p>
            </div>
            <!-- end of single instruction -->
            <!-- single instruction -->
            <div class="single-instruction">
              <header>
                <p>Step 3</p>
                <div></div>
              </header>
              <p>
                <b>Shape the Jamun Balls:</b>
                Divide the dough into small portions and roll them into smooth balls, ensuring there are no cracks.
              </p>
            </div>
            <!-- end of single instruction -->
            <!-- single instruction -->
            <div class="single-instruction">
              <header>
                <p>Step 4</p>
                <div></div>
              </header>
              <p>
                <b>Fry the Jamuns:</b>
                Heat oil or ghee in a deep pan on low-medium heat. Fry the balls in small batches,
                 stirring gently, until they turn golden brown. Remove and place them on a paper towel.
              </p>
            </div>
            <!-- end of single instruction -->
            <!-- single instruction -->
            <div class="single-instruction">
              <header>
                <p>Step 5</p>
                <div></div>
              </header>
              <p>
                <b>Soak in Syrup:</b>
While still warm, transfer the fried jamuns into the warm sugar syrup.
 Let them soak for at least 1-2 hours to absorb the syrup.
              </p>
            </div>
            <!-- end of single instruction -->
            <!-- single instruction -->
            <div class="single-instruction">
              <header>
                <p>Step 6</p>
                <div></div>
              </header>
              <p>
               <b>Serve and Enjoy:</b>
                Serve the gulab jamuns warm or chilled as desired.
             Garnish with chopped nuts like almonds or pistachios for added flavor.
            </div>
            <!-- end of single instruction -->
