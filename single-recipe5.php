                 The name translates to "rose berry," referencing the rose-flavored syrup and the dessert's berry-like appearance. Often served warm or chilled, 
                 this melt-in-your-mouth treat is a staple at celebrations and festivals, loved for its rich, indulgent sweetness and aromatic taste.
