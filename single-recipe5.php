              <p class="single-ingredient">1 cup milk powder</p>
              <p class="single-ingredient">1/4 cup all-purpose flour</p>
              <p class="single-ingredient">2 tbsp ghee (clarified butter)</p>
              <p class="single-ingredient">A pinch of baking soda</p>
              <p class="single-ingredient">Milk as needed (around 2-3 tbsp)</p>
              <p class="single-ingredient">1.5 cups sugar</p>
              <p class="single-ingredient">2 cups water</p>
              <p class="single-ingredient">3-4 cardamom pods (crushed)</p>
              <p class="single-ingredient">A few strands of saffron (optional)</p>
              <p class="single-ingredient">1 tsp rose water (optional)</p>
